# Assign a string value to strMyText string variable
strMyText = " Hello, Canada! "

#Upper Case
print(strMyText.upper())

#Lower Case
print(strMyText.lower())

#Remove Whitespace ----> The strip() method removes any whitespace from the beginning or the end:
print(strMyText.strip())

#Replace String
print(strMyText.replace("Canada","USA"))

#Split String
print(strMyText.split(","))

